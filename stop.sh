#!/bin/bash
# 申盾智能安全平台 停止脚本

# 获取运行在 3000 端口的进程 PID
PID=$(lsof -t -i :3000)

if [ -z "$PID" ]; then
    echo "========================================"
    echo "  平台已经处于关闭状态 (未发现正在运行的进程)"
    echo "========================================"
else
    echo "========================================"
    echo "  正在尝试关闭 申盾智能安全平台..."
    echo "  正在结束进程 PID: $PID"
    kill $PID
    echo "  平台已成功停止。"
    echo "========================================"
fi
